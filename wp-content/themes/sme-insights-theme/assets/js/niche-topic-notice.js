(function() {
	'use strict';
	
	if (typeof wp === 'undefined' || !wp.data || !wp.data.dispatch) {
		return;
	}
	
	const { dispatch } = wp.data;
	const { createWarningNotice } = dispatch('core/notices');
	
	// Wait for editor to be ready
	wp.domReady(function() {
		setTimeout(function() {
			const noticeMessage = 'This page uses the "Niche Topic Page" template. The design (Hero Section, Essential Guides, Latest News, Tools & Resources, Experts, Newsletter) is automatically generated by the template. You can view the full design by visiting the page on the frontend.';
			
			createWarningNotice(noticeMessage, {
				id: 'sme-niche-topic-template-notice',
				isDismissible: true,
			});
		}, 1000);
	});
})();

